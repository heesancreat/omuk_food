package com.example.mbplz;

public class User {
    private String profile;
    private String id;
    private String st;
    private String PN;
    private String LC;

    public String getProfile() {
        return profile;
    }

    public void setProfile(String profile) {
        this.profile = profile;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSt() {
        return st;
    }

    public void setSt(String st) {
        this.st = st;
    }

    public String getPN() {
        return PN;
    }

    public void setPN(String PN) {
        this.PN = PN;
    }

    public String getLC() {
        return LC;
    }

    public void setLC(String LC) {
        this.LC = LC;
    }

    public String getHr() {
        return Hr;
    }

    public void setHr(String hr) {
        Hr = hr;
    }

    private String Hr;

    public User(){}




}
